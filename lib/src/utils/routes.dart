int getRouteIndex(String route) {
  if (route == 'Buttons') {
    return 1;
  } else if (route == 'Rating') {
    return 2;
  } else if (route == 'Badge') {
    return 3;
  } else if (route == 'Toast') {
    return 4;
  } else if (route == 'Cupertino Icons') {
    return 5;
  } else if (route == 'Form Elements') {
    return 6;
  } else if (route == 'Form Validation') {
    return 7;
  } else if (route == 'Form File Upload') {
    return 8;
  } else if (route == 'Form Repeater') {
    return 9;
  } else if (route == 'Form Mask') {
    return 10;
  } else {
    return 0;
  }
}
